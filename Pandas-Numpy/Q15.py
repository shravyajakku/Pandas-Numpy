#compare two arrays using numpy.

import numpy as np

Array_a = np.array([1, 2])
print("Array a:", Array_a)
Array_b = np.array([4, 5])
print("Array b:", Array_b)
print("a>b")
print(Array_a > Array_b)
print("a>=b")
print(Array_a >= Array_b)
print("a<b")
print(Array_a < Array_b)
print("a<=b")
print(Array_a <= Array_b)
